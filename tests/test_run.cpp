// Serialize a TetMesh to the .mesh format, matching the write_mesh_aux logic in MeshIO.cpp
// for output_format == "mesh" (with only_interior = false, i.e. skip is_removed).
static std::string to_mesh_string(const floatTetWild::TetMesh& tm)
{
    std::ostringstream f;
    f.precision(std::numeric_limits<double>::digits10 + 1);

    f << "MeshVersionFormatted 1" << "\n";
    f << "Dimension 3" << "\n";

    f << "Vertices" << "\n" << tm.vertices.size() << "\n";
    for (const auto& v : tm.vertices)
        f << v.x << " " << v.y << " " << v.z << " " << 0 << "\n";

    f << "Triangles" << "\n" << 0 << "\n";

    f << "Tetrahedra" << "\n" << tm.tetrahedra.size() << "\n";
    for (const auto& t : tm.tetrahedra)
        f << (t.v[0] + 1) << " " << (t.v[1] + 1) << " "
          << (t.v[2] + 1) << " " << (t.v[3] + 1) << " " << 0 << "\n";

    f << "End";
    return f.str();
}

